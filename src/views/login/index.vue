<template>
  <div class="login-container">
    <el-row class="login-wrapper" type="flex">
      <el-col :md="10" class="login-aside"></el-col>
      <el-col :md="14">
        <el-form
          ref="loginForm"
          :model="loginForm"
          :rules="loginRules"
          class="login-form"
          autocomplete="on"
          label-position="left"
        >
          <div class="title-container">
            <h3 class="title">{{ defaultSettings.title }}</h3>
          </div>

          <el-form-item prop="account">
            <el-input
              ref="account"
              v-model="loginForm.account"
              placeholder="请填写登录账号"
              name="account"
              type="text"
              tabindex="1"
              autocomplete="on"
              @change="accountChange"
            >
              <template v-slot:prefix>
                <span class="svg-container">
                  <svg-icon icon-class="login-user" />
                </span>
              </template>
            </el-input>
          </el-form-item>

          <el-tooltip
            v-model="capsTooltip"
            content="大写锁定打开"
            placement="right"
            manual
          >
            <el-form-item prop="password">
              <el-input
                :key="passwordType"
                ref="password"
                v-model="loginForm.password"
                :type="passwordType"
                placeholder="请输入密码"
                name="password"
                tabindex="2"
                autocomplete="on"
                @keyup.native="checkCapslock"
                @blur="capsTooltip = false"
                @keyup.enter.native="handleLogin"
              >
                <template v-slot:prefix>
                  <span class="svg-container">
                    <svg-icon icon-class="login-password" />
                  </span>
                </template>

                <template v-slot:suffix>
                  <span class="show-pwd" @click="showPwd">
                    <svg-icon
                      :icon-class="
                        passwordType === 'password' ? 'eye' : 'eye-open'
                      "
                    />
                  </span>
                </template>
              </el-input>
            </el-form-item>
          </el-tooltip>

          <el-form-item prop="remember" size="mini">
            <el-row>
              <el-col :span="12">
                <el-checkbox v-model="loginForm.remember">
                  记住密码
                </el-checkbox>
              </el-col>
              <el-col :span="12" class="is-right">
                <el-popover
                  placement="top-end"
                  width="200"
                  trigger="hover"
                  content="请联系管理员找回密码。"
                >
                  <el-button slot="reference" type="text">忘记密码</el-button>
                </el-popover>
              </el-col>
            </el-row>
          </el-form-item>

          <el-button
            :loading="loading"
            type="primary"
            style="width: 100%;"
            @click.native.prevent="handleLogin"
          >
            登录
          </el-button>
        </el-form>
      </el-col>
    </el-row>
  </div>
</template>

<script>
import $store from '@/utils/$store'
import defaultSettings from '@/settings'
// import SocialSign from './components/SocialSignin'

export default {
  name: 'Login',
  // components: { SocialSign },
  data() {
    const validatePassword = (rule, value, callback) => {
      if (value.length < 5) {
        callback(new Error('密码不能小于5位字符'))
      } else {
        callback()
      }
    }
    return {
      loginForm: {
        account: '',
        password: '',
        code: '',
        imgCode: '',
        remember: false,
      },
      loginRules: {
        account: [{ required: true, trigger: 'blur', message: '请输入账号' }],
        code: [{ required: true, trigger: 'blur', message: '请输入验证码' }],
        password: [
          { required: true, trigger: 'blur', validator: validatePassword },
        ],
      },
      passwordType: 'password',
      capsTooltip: false,
      loading: false,
      showDialog: false,
      redirect: undefined,
      otherQuery: {},
      verification: '',
    }
  },
  computed: {
    defaultSettings() {
      return defaultSettings
    },
  },
  watch: {
    $route: {
      handler: function (route) {
        const query = route.query
        if (query) {
          this.redirect = query.redirect
          this.otherQuery = this.getOtherQuery(query)
        }
      },
      immediate: true,
    },
  },
  created() {
    const account = $store.loadAccount()
    if (account)
      this.loginForm = Object.assign(
        {},
        Array.isArray(account) ? account[account.length - 1] : account,
      )
    // window.addEventListener('storage', this.afterQRScan)
  },
  mounted() {
    if (this.loginForm.account === '') {
      this.$refs.account.focus()
    } else if (this.loginForm.password === '') {
      this.$refs.password.focus()
    }
  },
  destroyed() {
    // window.removeEventListener('storage', this.afterQRScan)
  },
  methods: {
    checkCapslock(e) {
      const { key } = e
      this.capsTooltip = key && key.length === 1 && key >= 'A' && key <= 'Z'
    },
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },

    accountChange() {
      const account = $store.loadAccount()
      if (account) {
        if (Array.isArray(account)) {
          let accountItem = account.filter(
            item => this.loginForm.account == item.account,
          )
          if (accountItem.length > 0) {
            this.loginForm.password = accountItem[0].password
          } else {
            this.loginForm.password = ''
          }
        }
      }
    },
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true
          this.$store
            .dispatch('user/login', this.loginForm)
            .then(() => {
              if (this.loginForm.remember) {
                $store.saveAccount(this.loginForm)
              }
              this.$router.push({
                path: '/',
                query: this.otherQuery,
                // path: this.redirect || '/',
              })
              this.loading = false
              //获取用户详情
              this.$store.dispatch('user/getInfo')
            })
            .catch(() => {
              this.loading = false
            })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    },
    getOtherQuery(query) {
      return Object.keys(query).reduce((acc, cur) => {
        if (cur !== 'redirect') {
          acc[cur] = query[cur]
        }
        return acc
      }, {})
    },
  },
}
</script>

