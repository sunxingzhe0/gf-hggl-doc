/**
 * 快排
 * @param {number[]} arr
 * @returns {number[]}
 */
export function quickSort(arr) {
  if (!arr || arr.length < 2) return arr

  const pivot = arr.pop()
  const { left, right } = arr.reduce(
    (_, item) => (item < pivot ? _.left.unshift(item) : _.right.push(item), _),
    {
      left: [],
      right: [],
    },
  )
  return [...quickSort(left), pivot, ...quickSort(right)]
}

