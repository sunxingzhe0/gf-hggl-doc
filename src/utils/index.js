/**
 * Check if an element has a class
 * @param {HTMLElement} elm
 * @param {string} cls
 * @returns {boolean}
 */
export function hasClass(ele, cls) {
  return !!ele.className.match(new RegExp('(\\s|^)' + cls + '(\\s|$)'))
}

/**
 * Add class to element
 * @param {HTMLElement} elm
 * @param {string} cls
 */
export function addClass(ele, cls) {
  if (!hasClass(ele, cls)) ele.className += ' ' + cls
}

/**
 * Remove class from element
 * @param {HTMLElement} elm
 * @param {string} cls
 */
export function removeClass(ele, cls) {
  if (hasClass(ele, cls)) {
    const reg = new RegExp('(\\s|^)' + cls + '(\\s|$)')
    ele.className = ele.className.replace(reg, ' ')
  }
}

/**
 * @callback requestCallback
 * @param {Object} node
 * @param {Object} parentNode
 * @returns {Boolean} filter tag
 */
/**
 * @param {Object | Object[]} node
 * @param {string} children - children prop
 * @param {requestCallback} callback
 */
export function traverseTree(
  data,
  children = 'children',
  callback = () => true,
) {
  if (!data) return data
  const nodes = isArray(data) ? deepClone(data) : [deepClone(data)]

  let index = 0
  while (nodes[index]) {
    const node = nodes[index]
    const r = callback(node)
    const tmp = []
    if (r) {
      if (isArray(node[children]) && 0 !== node[children].length)
        tmp.push(...node[children].map(_ => ({ ..._, parent: node })))

      nodes.splice(index, 1, r, ...tmp)
      tmp.length = 0
    }
    index++
  }
  return nodes
}

